def sum_two_number(a, b):
    x = a + b
    print(f'Сумма {a} + {b} = {a + b}')
    return x


def difference(a, b):
    print(f'разность {a} - {b} = {a - b}')
    return a-b

def multiply(a, b):
    print(f'умножить {a} * {b} = {a * b}')
    return a*b


def share(a, b):
    print(f'разделить {a} / {b} = {a / b}')
    return a/b